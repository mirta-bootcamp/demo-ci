
public class Math {
	
	/***
	 * Sum 2 integers
	 * @param x
	 * @param y
	 * @return the result as integer
	 */
	public static int sum(int x, int y) {
		return x+y;
	}
	
	/**
	 * Divide 2 integers
	 * @param x
	 * @param y
	 * @return the result as integer
	 */
	public static int divide(int x, int y) {
		return (int) x/y;
	}

}
